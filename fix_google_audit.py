def get_city_from_path(filepath):
    for part in filepath.parts:
        if part in UNIQUE_INTROS:
            return part
    return None

def get_category_from_path(filepath):
    for part in filepath.parts:
        if part in ['food','beach','culture','nature','shopping','transport']:
            return part
    return None

def improve_html(filepath):
    """HTML 파일 전면 개선"""
    with open(filepath, 'r', encoding='utf-8') as f:
        content = f.read()
    
    city = get_city_from_path(filepath)
    category = get_category_from_path(filepath)
    
    if not city or not category:
        return False
    
    original = content
    rng = random.Random(hash(f"{city}_{category}_{filepath.stem}"))
    
    # 1. "핵심 정보" H2를 도시별 고유 H2로 교체
    if '핵심 정보' in content:
        intros = UNIQUE_INTROS.get(city, {}).get(category, ["이 도시의 이야기"])
        chosen = rng.choice(intros)
        content = content.replace(
            '>핵심 정보</h2>',
            f'>{chosen}</h2>',
            1  # 첫 번째만 교체
        )
    
    # 2. "마무리 정리" H2 다양화
    if '마무리 정리' in content:
        ending = rng.choice(VARIED_ENDINGS)
        content = content.replace('>마무리 정리</h2>', f'>{ending}</h2>')
    
    # 3. "자주 묻는 질문" H2 다양화
    if '자주 묻는 질문' in content:
        faq = rng.choice(VARIED_FAQ)
        content = content.replace('>자주 묻는 질문</h2>', f'>{faq}</h2>')
    
    # 4. "관련 지역 추천" H2 다양화
    related_variants = [
        f"근처에 가볼 만한 곳",
        f"{city} 주변 탐방",
        f"같이 가면 좋은 곳",
        f"다음 코스 추천",
        f"이어서 방문할 곳",
    ]
    if '관련 지역 추천' in content:
        related = rng.choice(related_variants)
        content = content.replace('>관련 지역 추천</h2>', f'>{related}</h2>')
    
    # 5. 환율 템플릿 제거
    content = content.replace('환율 앱', '환전 팁')
    
    if content != original:
        with open(filepath, 'w', encoding='utf-8') as f:
            f.write(content)
        return True
    return False

