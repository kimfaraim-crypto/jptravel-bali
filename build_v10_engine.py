def generate_body_paragraphs(city, cat, idx, data, items):
    """카테고리별 고유 본문 문단 생성"""
    paragraphs = []
    city_data = data.get(city, {})
    vibe = city_data.get("vibe", "")
    
    # 인트로에서 이미 사용된 문장 제외하고 본문 생성
    if cat == "food" and items:
        # 음식점 상세 소개 (2~3곳 선택)
        selected = items[:min(3, len(items))]
        for item in selected:
            p = f'{item["name"]}({item["area"]})는 '
            if "must" in item:
                p += f'{item["must"]} 메뉴가 시그니처예요. '
            p += f'{item["tip"]} '
            if "addr" in item:
                p += f'주소는 {item["addr"]}예요.'
            if "phone" in item:
                p += f' 전화번호는 {item["phone"]}예요.'
            paragraphs.append(p)
        
        # 가격 정보
        if city_data.get("price_comp"):
            pc = city_data["price_comp"]
            p = f'{city} 물가 참고: '
            p += ", ".join([f'{x["item"]} 로컬 {x["local"]}' for x in pc[:3]])
            p += " 정도예요."
            paragraphs.append(p)
    
    elif cat == "culture" and items:
        selected = items[:min(3, len(items))]
        for item in selected:
            p = f'{item["name"]}은(는) {item["tip"]}'
            if "addr" in item:
                p += f' 주소: {item["addr"]}'
            p += "."
            paragraphs.append(p)
    
    elif cat == "beach" and items:
        selected = items[:min(3, len(items))]
        for item in selected:
            p = f'{item["name"]} - {item["tip"]}'
            if "price" in item and item["price"] != "무료":
                p += f' 가격: {item["price"]}'
            p += "."
            paragraphs.append(p)
    
    elif cat == "nature" and items:
        selected = items[:min(3, len(items))]
        for item in selected:
            p = f'{item["name"]} - {item["tip"]}'
            if "price" in item:
                p += f' 입장료: {item["price"]}'
            p += "."
            paragraphs.append(p)
    
    elif cat == "shopping" and items:
        selected = items[:min(3, len(items))]
        for item in selected:
            p = f'{item["name"]}({item["area"]}) - {item["tip"]}'
            if "price" in item:
                p += f' 가격: {item["price"]}'
            p += "."
            paragraphs.append(p)
    
    elif cat == "transport" and items:
        for item in items:
            p = f'{item["name"]}: {item["price"]}. {item["tip"]}'
            paragraphs.append(p)
    
    # 공통: 호텔 정보
    hotels = city_data.get("hotels", {})
    if hotels:
        p = f'{city} 숙소: '
        parts = []
        if "budget" in hotels:
            parts.append(f'예산형 {hotels["budget"]}')
        if "mid" in hotels:
            parts.append(f'중급 {hotels["mid"]}')
        if "high" in hotels:
            parts.append(f'고급 {hotels["high"]}')
        p += ", ".join(parts) + "."
        paragraphs.append(p)
    
    # 공통: 숨은 명소
    hidden = city_data.get("hidden", "")
    if hidden:
        paragraphs.append(f'숨은 명소 팁: {hidden}')
    
    return paragraphs

