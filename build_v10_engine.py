def generate_internal_links(city, cat, page_num):
    """같은 도시 다른 카테고리 + 다른 도시 같은 카테고리 크로스링크"""
    links = []
    
    # 같은 도시 다른 카테고리 (랜덤 2개)
    other_cats = OTHER_CATS.get(cat, [])
    selected_cats = random.sample(other_cats, min(2, len(other_cats)))
    for oc in selected_cats:
        links.append({
            "text": f'{city} {CATEGORIES[oc]["name"]} 가이드',
            "href": f'/{city}/{oc}/001.html',
            "type": "same_city"
        })
    
    # 다른 도시 같은 카테고리 (랜덤 2개)
    other_cities = [a for a in ["우붓", "스미냑", "꾸따", "사누르", "누사두아", "울루와뚜"] if a != city]
    selected_cities = random.sample(other_cities, min(2, len(other_cities)))
    for oc in selected_cities:
        links.append({
            "text": f'{oc} {CATEGORIES[cat]["name"]} 가이드',
            "href": f'/{oc}/{cat}/001.html',
            "type": "other_city"
        })
    
    return links

