def seeded_random(seed_str, idx=0):
    seed_val = int(hashlib.md5(f"{seed_str}_{idx}".encode()).hexdigest()[:8], 16)
    return random.Random(seed_val)


def fix_round1_qa():
    """Round 1: Q&A 답변 중복 해결 (전 카테고리)"""
    print("=" * 60)
    print("Round 1: Q&A 답변 중복 해결")
    print("=" * 60)
    fixed = 0
    for f in sorted(HTML_DIR.rglob("*.html")):
        with open(f) as fh:
            content = fh.read()
        parts = f.relative_to(HTML_DIR).parts
        if len(parts) < 3:
            continue
        area, cat = parts[0], parts[1]
        page_num = int(parts[2].replace(".html", ""))

        if cat not in QA_FULL or area not in QA_FULL[cat]:
            continue

        answers = QA_FULL[cat][area]
        rng = seeded_random(f"qa_{area}_{cat}_{page_num}")
        shuffled = list(answers)
        rng.shuffle(shuffled)

        # Find Q&A section
        qa_section_match = re.search(
            r"(❓ 자주 묻는 질문</h2>)(.*?)(<h2>)", content, re.DOTALL
        )
        if not qa_section_match:
            continue

        qa_block = qa_section_match.group(2)
        answer_pattern = r'(<p style="margin:0;color:#333;line-height:1.7">)(.*?)(</p>)'
        answer_matches = list(re.finditer(answer_pattern, qa_block))

        if not answer_matches:
            continue

        new_qa = qa_block
        for i, match in enumerate(answer_matches):
            old_answer = match.group(2)
            if i < len(shuffled):
                new_answer = shuffled[i]
            else:
                new_answer = shuffled[i % len(shuffled)]
            new_qa = new_qa.replace(
                f"{match.group(1)}{old_answer}{match.group(3)}",
                f"{match.group(1)}{new_answer}{match.group(3)}",
                1,
            )

        if new_qa != qa_block:
            content = content.replace(qa_block, new_qa)
            with open(f, "w", encoding="utf-8") as fh:
                fh.write(content)
            fixed += 1

    print(f"수정된 파일: {fixed}")
    return fixed


def fix_round2_titles():
    """Round 2: 중복 제목 해결"""
    print()
    print("=" * 60)
    print("Round 2: 중복 제목 해결")
    print("=" * 60)

    title_map = {}
    for f in sorted(HTML_DIR.rglob("*.html")):
        with open(f) as fh:
            content = fh.read()
        title_m = re.search(r"<title>(.*?)</title>", content)
        if title_m:
            title_map.setdefault(title_m.group(1), []).append(f)

    fixed = 0
    for title, files in title_map.items():
        if len(files) <= 1:
            continue
        for i, f in enumerate(files[1:], 1):
            with open(f) as fh:
                content = fh.read()
            parts = f.relative_to(HTML_DIR).parts
            area, cat, page = parts[0], parts[1], parts[2].replace(".html", "")
            # Add page-specific suffix to make title unique
            cat_names = {
                "food": "맛집", "culture": "문화", "beach": "해변",
                "nature": "자연", "shopping": "쇼핑", "transport": "교통",
            }
            cat_name = cat_names.get(cat, cat)
            suffix_map = {
                "001": "핵심 코스", "002": "숨은 명소", "003": "가성비 추천",
                "004": "현지인 팁", "005": "포토스팟", "006": "액티비티",
                "007": "힐링 코스", "008": "맛집 투어", "009": "쇼핑 가이드",
                "010": "교통 정보", "011": "숙소 추천", "012": "예산 정리",
                "013": "일정 추천", "014": "종합 가이드",
            }
            suffix = suffix_map.get(page, f"파트 {page}")
            new_title = f"{area} {cat_name} {suffix} — {page}편 ({2026})"
            # Replace in title tag
            content = content.replace(f"<title>{title}</title>", f"<title>{new_title}</title>")
            # Replace in og:title
            og_m = re.search(r'content="og:title" content="(.*?)"', content)
            if not og_m:
                content = re.sub(
                    r'(<meta property="og:title" content=")(.*?)(")',
                    f'\\g<1>{new_title}\\g<3>',
                    content,
                )
            # Replace in h1
            h1_m = re.search(r"<h1>(.*?)</h1>", content)
            if h1_m:
                content = content.replace(f"<h1>{h1_m.group(1)}</h1>", f"<h1>{new_title}</h1>")

            with open(f, "w", encoding="utf-8") as fh:
                fh.write(content)
            fixed += 1

    print(f"수정된 파일: {fixed}")
    return fixed


def fix_round3_mapping():
    """Round 3: 이미지 매핑 누락 해결"""
    print()
    print("=" * 60)
    print("Round 3: 이미지 매핑 누락 해결")
    print("=" * 60)

    with open(BASE_DIR / "image_mapping_v3.json") as f:
        mapping = json.load(f)

    fixed = 0
    for area in list(mapping.keys()):
        for cat in list(mapping[area].keys()):
            imgs = mapping[area][cat]
            missing = [img for img in imgs if not (IMG_DIR / area / cat / img).exists()]
            if missing:
                # Remove missing entries
                mapping[area][cat] = [img for img in imgs if img not in missing]
                fixed += len(missing)
                print(f"  ❌ {area}/{cat}: {len(missing)} missing images removed")

    with open(BASE_DIR / "image_mapping_v3.json", "w") as f:
        json.dump(mapping, f, ensure_ascii=False, indent=2)

    print(f"총 제거된 누락 이미지: {fixed}")
    return fixed


