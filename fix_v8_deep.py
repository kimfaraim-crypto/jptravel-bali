def generate_diverse_qa(area, category, page_idx, data):
    """Q&A 답변을 페이지별로 다양하게 생성"""
    rng = random.Random(hash(f"{area}_{category}_{page_idx}_qa_v8"))

    qa_pool = data.get("intro_qa", [])
    if not qa_pool:
        return ""

    # 페이지별로 다른 Q&A 선택
    q, a = qa_pool[page_idx % len(qa_pool)]

    # 답변 변형
    a_variants = [
        a,
        f"솔직하게 말하면, {a} 이건 제 경험 기준이에요.",
        f"직접 가본 결과, {a} 더 자세한 정보는 본문에서 확인하세요.",
        f"현지인에게 물어본 결과, {a} 가격은 변동될 수 있어요.",
    ]

    answer = a_variants[page_idx % len(a_variants)]
    answer = fix_josa(answer, area)

    return f'''<div style="margin:20px 0;padding:20px;background:linear-gradient(135deg,#e8f5e9,#f1f8e9);border-radius:12px;border:1px solid #c8e6c9">
<p style="margin:0 0 8px;font-weight:700;color:#2e7d32;font-size:1.05em">❓ {q}</p>
<p style="margin:0;color:#333;line-height:1.7">{answer}</p>
</div>'''


