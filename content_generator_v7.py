        lambda: f"결론부터 말하면, {area}의 {cat_name}은(는) {best_season}이 최적 시기예요. {first_spot}를 꼭 방문하시고, {first_food}에서 식사하는 걸 추천해요. 예산형으로도 충분히 즐길 수 있으니 걱정 마세요.",

